void	ft_putchar(char c);
void	print(int a, char f, char m, char e);

void	rush(int a, int b)
{
	if (a > 0 && b > 0)
	{
		print(a, 'A', 'B', 'A');
		b--;
		while (b > 1)
		{
			print(a, 'B', ' ', 'B');
			b--;
		}
		if (b > 0)
		{
			print(a, 'C', 'B', 'C');
		}
	}
}
