int		ft_sqrt(int nb)
{
	int i;
	int square;

	square = 0;
	i = 1;
	while (i <= nb / 2)
	{
		square = i * i;
		if (square == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
