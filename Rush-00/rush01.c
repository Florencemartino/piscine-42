void	ft_putchar(char c);
void	print(int a, char f, char m, char e);

void	rush(int a, int b)
{
	if (a > 0 && b > 0)
	{
		print(a, '/', '*', '\\');
		b--;
		while (b > 1)
		{
			print(a, '*', ' ', '*');
			b--;
		}
		if (b > 0)
		{
			print(a, '\\', '*', '/');
		}
	}
}
