#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (argv[1][i] != '\0')
	{
		if (argv[1][i] == argv[2][j])
		{
			write(1, &argv[1][j], 1);
			j++;
		}
		i++;
	}

	write(1, "\n", 1);
	return 0;
}



